// ============================================================================
// INPCAffinityConfig.cs - NPC好感度配置接口
// ============================================================================
// 模块说明：
//   定义NPC好感度配置的通用接口，每个NPC实现此接口以提供个性化配置。
//   支持多NPC扩展，新NPC只需实现此接口并注册到 AffinityManager 即可。
//   对话功能请实现 INPCDialogueConfig 接口。
// ============================================================================

using System.Collections.Generic;

namespace BossRush
{
    /// <summary>
    /// NPC好感度配置接口
    /// <para>每个自定义NPC实现此接口以提供个性化的好感度配置</para>
    /// </summary>
    public interface INPCAffinityConfig
    {
        // ============================================================================
        // NPC标识
        // ============================================================================
        
        /// <summary>
        /// NPC唯一标识符（如 "goblin_dingdang"）
        /// </summary>
        string NpcId { get; }
        
        /// <summary>
        /// NPC显示名称（已本地化）
        /// </summary>
        string DisplayName { get; }
        
        // ============================================================================
        // 好感度配置
        // ============================================================================
        
        /// <summary>
        /// 最大好感度点数（默认1000）
        /// </summary>
        int MaxPoints { get; }
        
        /// <summary>
        /// 每级所需点数（默认100）
        /// </summary>
        int PointsPerLevel { get; }
        
        /// <summary>
        /// 最大等级（默认10）
        /// </summary>
        int MaxLevel { get; }
        
        // ============================================================================
        // 礼物配置
        // ============================================================================
        
        /// <summary>
        /// 礼物好感度增益字典（物品类型 -> 好感度增益）
        /// </summary>
        Dictionary<string, int> GiftValues { get; }
        
        // ============================================================================
        // 解锁配置
        // ============================================================================
        
        /// <summary>
        /// 等级解锁内容字典（等级 -> 解锁内容描述列表）
        /// </summary>
        Dictionary<int, string[]> UnlocksByLevel { get; }
        
        /// <summary>
        /// 等级折扣字典（等级 -> 折扣率，如0.05表示5%折扣）
        /// </summary>
        Dictionary<int, float> DiscountsByLevel { get; }
    }
}
