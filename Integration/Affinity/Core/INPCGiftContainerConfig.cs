// ============================================================================
// INPCGiftContainerConfig.cs - NPC礼物容器配置接口
// ============================================================================
// 扩展 INPCGiftConfig 接口，添加容器式UI的配置选项。
// 实现此接口的NPC将使用容器式礼物赠送UI（类似快递员服务）。
// ============================================================================

namespace BossRush
{
    /// <summary>
    /// NPC礼物容器配置接口
    /// <para>扩展 INPCGiftConfig，添加容器式UI的配置选项</para>
    /// <para>实现此接口的NPC将使用单格容器UI进行礼物赠送</para>
    /// </summary>
    public interface INPCGiftContainerConfig : INPCGiftConfig
    {
        // ============================================================================
        // 容器UI文本配置
        // ============================================================================
        
        /// <summary>
        /// 容器标题本地化键
        /// <para>显示在容器UI顶部的标题文本</para>
        /// <para>例如："赠送礼物给叮当"</para>
        /// </summary>
        string ContainerTitleKey { get; }
        
        /// <summary>
        /// 赠送按钮文本本地化键
        /// <para>显示在赠送按钮上的文本</para>
        /// <para>例如："赠送"</para>
        /// </summary>
        string GiftButtonTextKey { get; }
        
        /// <summary>
        /// 空槽位提示文本本地化键
        /// <para>当容器为空时显示的提示文本</para>
        /// <para>例如："放入礼物"</para>
        /// </summary>
        string EmptySlotTextKey { get; }
        
        // ============================================================================
        // UI行为配置
        // ============================================================================
        
        /// <summary>
        /// 是否使用容器式UI
        /// </summary>
        bool UseContainerUI { get; }
    }
}
